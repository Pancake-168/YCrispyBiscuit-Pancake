def _get_wbi_keys() -> dict[str, str]:
    global _wbi_cache, _wbi_cache_time
    if _wbi_cache and time.time() - _wbi_cache_time < 3600:
        return _wbi_cache
    resp = httpx.get(
        "https://api.bilibili.com/x/web-interface/nav",
        headers={"User-Agent": BILIBILI_USER_AGENT},
    )
    data = resp.json()["data"]
    img_url = data["wbi_img"]["img_url"]
    sub_url = data["wbi_img"]["sub_url"]
