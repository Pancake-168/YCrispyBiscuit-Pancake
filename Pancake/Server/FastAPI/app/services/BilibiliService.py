def _get_mixin_key(orig: str) -> str:
    return "".join(orig[i] for i in MIXIN_KEY_ENC_TAB)[:32]


def _sign_params(params: dict[str, Any]) -> dict[str, Any]:
    """Wbi sign the request parameters."""
    mixin_key = _get_mixin_key(_get_wbi_keys()["img_key"] + _get_wbi_keys()["sub_key"])
    params_sorted = dict(sorted(params.items()))
    params_sorted["wts"] = int(time.time())
    query = urllib.parse.urlencode(params_sorted)
    sign = hashlib.md5((query + mixin_key).encode()).hexdigest()
    params_sorted["w_rid"] = sign
    return params_sorted


